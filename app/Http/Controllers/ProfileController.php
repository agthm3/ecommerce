    }

    public function edit_profile(Request $request){
        $request->validate([
            'name' => 'required',
            'password' => 'required|min:8|confirmed'
        ]);
        $user = Auth::user();

        $user->update([
            'name' => $request->name, 
            'password' => Hash::make($request->password)
        ]);

        return Redirect::back();
    }

